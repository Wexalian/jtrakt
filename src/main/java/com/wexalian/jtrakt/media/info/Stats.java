package com.wexalian.jtrakt.media.info;

public class Stats
{
    private int watchers;
    private int plays;
    private int collectors;
    private int collected_episodes;
    private int comments;
    private int lists;
    private int votes;
    
    public int getWatchers()
    {
        return watchers;
    }
    
    public int getPlays()
    {
        return plays;
    }
    
    public int getCollectors()
    {
        return collectors;
    }
    
    public int getCollectedEpisodes()
    {
        return collected_episodes;
    }
    
    public int getComments()
    {
        return comments;
    }
    
    public int getLists()
    {
        return lists;
    }
    
    public int getVotes()
    {
        return votes;
    }
}
